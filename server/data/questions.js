// Dynamic total rounds based on player count
export function calcTotalRounds(playerCount) {
  if (playerCount <= 3)  return 5;
  if (playerCount <= 6)  return 7;
  if (playerCount <= 10) return 10;
  if (playerCount <= 15) return 12;
  return 15;
}

// Get difficulty for a given round index based on total rounds
export function getDifficulty(roundIdx, totalRounds) {
  const progress = roundIdx / totalRounds;
  if (progress < 0.3)  return "easy";
  if (progress < 0.6)  return "medium";
  if (progress < 0.85) return "hard";
  return "extreme";
}
