// Activity order per round (from easiest to hardest)
export function getActivityOrder(totalRounds) {
  const easy = ["reaction", "rate"];
  const medium = ["binary"];
  const text = ["word", "sentence"];
  const hard = ["rank"];
  const nuclear = ["who"];

  const order = [];
  for (let i = 0; i < totalRounds; i++) {
    if (i === 0) {
      order.push(easy[Math.floor(Math.random() * easy.length)]);
    } else if (i === 1) {
      order.push(medium[0]);
    } else if (i === 2) {
      order.push(text[Math.floor(Math.random() * text.length)]);
    } else if (i === 3) {
      order.push(hard[0]);
    } else {
      // Round 5+ โ "ููู ูููู" (nuclear), or repeat if more rounds
      if (!order.includes("who")) {
        order.push(nuclear[0]);
      } else {
        // Mix remaining types
        const remaining = ["reaction", "rate", "binary", "word", "sentence", "rank", "who"];
        const unused = remaining.filter((t) => !order.includes(t));
        order.push(unused.length > 0 ? unused[0] : remaining[Math.floor(Math.random() * remaining.length)]);
      }
    }
  }
  return order;
}

